package com.actionsmicro.media.videoobj;

// POJO
public class AudioCodec {
    private String ext;
    private String acodec;
    private int abr;
    private String src;

    public String getExt() {
        return ext;
    }

    public String getAcodec() {
        return acodec;
    }

    public int getAbr() {
        return abr;
    }

    public String getSrc() {
        return src;
    }
}
